#include <stdio.h>

int main() {
    // Character example
    char myChar = 'A';
    printf("Character: %c\n", myChar);

    // String example
    char myString[] = "Hello";
    printf("String: %s\n", myString);

    return 0;
}

// The output of the above program is shown as below:

/* 
Character: A
String: Hello
*/